package com.wuba.wlock.client.registryclient.entity;

public class Node {
    private String ip;
    private int port;
    private int sequence;

    public Node() {
    }

    public Node(String ip, int port, int sequence) {
        this.ip = ip;
        this.port = port;
        this.sequence = sequence;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public int getSequence() {
        return sequence;
    }

    public void setSequence(int sequence) {
        this.sequence = sequence;
    }
}
